local function CreateSetRule(suf, ruletext)
    local rulename = "LifeHistory"..suf.."RemoveHist"
    local fname = g.getdir("rules")..rulename..".rule"
    g.show(rulename.." "..fname)
    local f=io.open(fname,"r")
    if f~=nil then
        io.close(f)  -- rule already exists
    else 
        local f = io.open(fname, "w")
        if f then
            f:write(ruletext)
            f:close()
        else
            g.warn("Can't save "..rulename.." rule in fname:\n"..fname)
        end
    end
    g.setrule(rulename)
end

step = g.getstep()
if rule=="LifeHistory" then
    CreateSetRule("",ruletext)
elseif rule=="LifeHistory14" then
    CreateSetRule("14",rule14text)
else
    CreateSetRule("64",rule64text)
end
g.run(1)
g.setrule(rule)
g.setstep(step)
